<?php

namespace app\models;

use app\modules\EquipamentoMovimento\EquipamentoMovimento;
use app\modules\EquipamentoMovimentoTipo\EquipamentoMovimentoTipo;
use Yii;
use yii\db\ActiveRecord;


class Funcionario extends FuncionarioBase
{

	public const DESTINO_FRANCA = 82;
	public const DESTINO_FRANCE = 93;
	public const DESTINO_SEDE = 3;

	/**
	 * Ao receber um Equipamento de acordo com o último movimento devolve os destinos possiveis.
	 * @param $equipamento_id
	 * @return EquipamentoMovimentoTipo[]|array|ActiveRecord[]
	 */
	public static function getDestinoForEquipamento($equipamento_id)
	{

		##Vai buscar o último movimento
		$lastMovimento = EquipamentoMovimento::find()
			->where(['equipamento_id' => $equipamento_id])
			->orderBy(['id' => SORT_DESC])
			->one();

		if (empty($lastMovimento)) {
			##Se não existir último movimento consulta todas as possibilidades
			$data = Funcionario::find()
				->where(['tipo' => 'Localização', 'ativo' => 1])
				->orderBy(['nome' => SORT_ASC])
				->select(['id', 'nome AS name'])->asArray()->all();
		} else {
			if ($lastMovimento->tipo_movimento_id == 1) { ##Saida tem que entrar
				##Se existir consulta o tipo de movimento disponivel
				$data = Funcionario::find()
					->where(['tipo' => 'Localização', 'ativo' => 1])
					->orderBy(['nome' => SORT_ASC])
					->select(['id', 'nome AS name'])->asArray()->all();
			} else {  ##Entrou tem que sair
				$data = Funcionario::find()
					->where(['tipo' => 'Funcionário', 'ativo' => 1])
					->orderBy(['nome' => SORT_ASC])
					->select(['id', 'nome AS name'])->asArray()->all();
			}
		}

		return $data;
	}


	public function afterSave($insert, $changedAttributes)
	{

		Yii::$app->mailer->compose('layouts/destino', ['model' => $this])
			->setFrom('equipamentos@appa8.com')
			->setTo(Yii::$app->params['equipamentoEmail'])
			->setSubject("Funcionário Criado/Alterado $this->id | $this->nome , data: " . date("d-m-Y H:i")
				. " | Utilizador: " . Yii::$app->user->identity->username)
			->setTextBody("Funcionário Criado/Alterado $this->nome , data: " . date("d-m-Y H:i"))
			->send();

		parent::afterSave($insert, $changedAttributes); // TODO: Change the autogenerated stub
	}


	/**
	 * Ao receber um Equipamento de acordo com o último movimento devolve os destinos possiveis.
	 * @param $equipamento_id
	 * @return EquipamentoMovimentoTipo[]|array|ActiveRecord[]
	 */
	public static function getDestinoUpdateForEquipamento($equipamento_id)
	{

		##Vai buscar o último movimento
		$lastMovimento = EquipamentoMovimento::find()
			->where(['equipamento_id' => $equipamento_id])
			->orderBy(['id' => SORT_DESC])
			->one();

		if (empty($lastMovimento)) {
			##Se não existir último movimento consulta todas as possibilidades
			$data = Funcionario::find()
				->where(['tipo' => 'Localização', 'ativo' => 1])
				->orderBy(['nome' => SORT_ASC])
				->select(['id', 'nome AS name'])->asArray()->all();
		} else {
			if (!$lastMovimento->tipo_movimento_id == 1) { ##Saida tem que entrar
				##Se existir consulta o tipo de movimento disponivel
				$data = Funcionario::find()
					->where(['tipo' => 'Localização', 'ativo' => 1])
					->orderBy(['nome' => SORT_ASC])
					->select(['id', 'nome AS name'])->asArray()->all();
			} else {  ##Entrou tem que sair
				$data = Funcionario::find()
					->where(['tipo' => 'Funcionário', 'ativo' => 1])
					->orderBy(['nome' => SORT_ASC])
					->select(['id', 'nome AS name'])->asArray()->all();
			}
		}

		return $data;
	}

	/**
	 * Ao receber um Equipamento de acordo com o último movimento devolve os destinos possiveis.
	 * @param $equipamento_id
	 * @return EquipamentoMovimentoTipo[]|array|ActiveRecord[]
	 */
	public static function getDestinoForEquipamento_withTipoMovimento($equipamento_id, $tipo_movimento_id)
	{

		if (empty($equipamento_id) or empty($tipo_movimento_id)) {
			return [];
		};

		if (in_array($tipo_movimento_id, [
			EquipamentoMovimentoTipo::MOVIMENTO_ENTRADA_ARMAZEM,
			EquipamentoMovimentoTipo::MOVIMENTO_ENTRADA,
			EquipamentoMovimentoTipo::MOVIMENTO_SAIDA_ARMAZEM
		])) { ##Saida tem que entrar
			##Se existir consulta o tipo de movimento disponivel
			return Funcionario::find()
				->where(['tipo' => 'Localização', 'ativo' => 1])
				->orderBy(['nome' => SORT_ASC])
				->select(['id', 'nome AS name'])->asArray()->all();
		}

		##Entrou tem que sair
		return Funcionario::find()
			->where(['tipo' => 'Funcionário', 'ativo' => 1])
			->orderBy(['nome' => SORT_ASC])
			->select(['id', 'nome AS name'])->asArray()->all();
	}
}
