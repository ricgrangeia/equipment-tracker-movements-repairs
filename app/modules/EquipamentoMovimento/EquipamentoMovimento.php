<?php

namespace app\modules\EquipamentoMovimento;

use app\models\Funcionario;
use app\models\Localization;
use app\modules\Equipamento\Domain\Entity\Equipamento;
use app\modules\EquipamentoMovimentoTipo\EquipamentoMovimentoTipo;
use Yii;
use yii\helpers\ArrayHelper;

class EquipamentoMovimento extends EquipamentoMovimentoBase
{
	/**
	 * {@inheritdoc}
	 */
	private $cached_equipamento_id;

	public function rules()
	{

		$rules = parent::rules();

		return ArrayHelper::merge($rules, [
			[['equipamento_id'], 'validateEquipamentoEmReparacao'],
		]);
	}


	public function validateEquipamentoMovimento($attribute, $params, $validator, $value)
	{

		yii::debug($value);
		//       yii::debug($validator);
		yii::debug($attribute);
		if (!Equipamento::isEmArmazem($this->equipamento_id) && $this->tipo_movimento_id == 2)
			$this->addError($attribute, 'Este Equipamento está fora, não pode ir para reparação!');
	}


	public function validateEquipamentoEmReparacao($attribute, $params, $validator, $value)
	{

		if (Equipamento::isEmReparacao($this->equipamento_id))
			$this->addError($attribute, 'Este Equipamento está em reparação! Não pode ser movimentado!');
	}


	/**
	 * Gets query for [[Destino]].
	 *
	 * @return \yii\db\ActiveQuery
	 */
	public function getLastDestino()
	{

		return ($this->hasOne(Funcionario::class, ['id' => 'destino_id'])
			->orderBy(['id' => SORT_DESC]))->destino->nome;
	}


	public function validateMovimentoTipo()
	{

		//        if (empty($this->equipamento_id)) {
		//            $this->addError('tipo_movimento_id', 'Tem que escolher Equipamento');
		//        }

		if ($this->tipoMovimento->tipo_movimento == $this->equipamento->getLastDestino()) {

			$this->addError('tipo_movimento_id', 'Start time should less than End time');
		}
	}

	/**
	 * Devolve Equipamentos que não estão em fim de vida
	 * @return Equipamento[]|array|\yii\db\ActiveRecord[]
	 */
	public static function getEquipamentosNaSede()
	{

		# Tipo Estado 3 -> Fim de Vida
		return EquipamentoMovimento::find()->where(["tipoMovimento_id" => 2])->andWhere(['equipamentoMovimentos.tipoMovimento'])->all();
	}


	/**
	 * Vai buscar o último movimento do equipamento
	 * @return EquipamentoMovimento|array|\yii\db\ActiveRecord|null
	 */
	private function getLastMovimento()
	{

		return EquipamentoMovimento::find()->where("equipamento_id = $this->equipamento_id")->orderBy(['id' => SORT_DESC])->one();
	}


	/**
	 * Verifica se é o último movimento do equipamento
	 * @return bool
	 */
	public function isLastMovimento()
	{

		if (empty($this)) return false;
		$lastMovimento = $this->getLastMovimento();
		if ($this->id == $lastMovimento->id) return true;

		return false;
	}

	public function afterSave($insert, $changedAttributes)
	{


		parent::afterSave($insert, $changedAttributes);
		// TODO: Change the autogenerated stub

		if ($this->tipo_movimento_id == EquipamentoMovimentoTipo::MOVIMENTO_SAIDA_ARMAZEM) {
			$equipamento = Equipamento::findOne($this->equipamento_id);
			$equipamento->localization_id = Localization::EM_TROCA_LOCALIZACAO;
			$equipamento->updateAttributes(['localization_id']);
		}

		if ($this->tipo_movimento_id == EquipamentoMovimentoTipo::MOVIMENTO_ENTRADA_ARMAZEM) {
			$equipamento = Equipamento::findOne($this->equipamento_id);
			if (in_array($this->destino_id, [Funcionario::DESTINO_FRANCA, Funcionario::DESTINO_FRANCE])) {
				$equipamento->localization_id = Localization::LOCALIZACAO_FRANCA;
			} else {
				$equipamento->localization_id = Localization::LOCALIZACAO_PORTUGAL;
			}

			$equipamento->updateAttributes(['localization_id']);
		}
	}
}
