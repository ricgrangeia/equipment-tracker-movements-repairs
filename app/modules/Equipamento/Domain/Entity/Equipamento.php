	/**
	 * @return string
	 */
	public function getLastDestino(): string
	{

		/** @var EquipamentoMovimento $model */
		$model = EquipamentoMovimento::find()
			->where(['equipamento_id' => $this->id])
			->orderBy(['id' => SORT_DESC])
			->one();
		if (empty($model))
			return '';

		return $model->destino->nome;
	}

