	/*************************************************
	 * EVENTS
	 *************************************************/
	/**
	 * @param $insert
	 * @return bool
	 */
	public function beforeSave($insert): bool
	{

		if (!parent::beforeSave($insert)) {
			return false;
		}

		if ($insert) {
			$this->localization_id = Localization::SEM_LOCALIZACAO_NOVO;
		}

		return true;
	}

	/**
	 * @param $insert
	 * @param $changedAttributes
	 * @return void
	 */
	public function afterSave($insert, $changedAttributes)
	{

		if ($this->isNewRecord) {
			$this->sendEmailEquipamentoNotification('Criado Novo');
		} else {
			if ($this->estado->shouldNotify() && $changedAttributes['estado_id'] !== $this->estado_id) {
				$this->sendEmailEquipamentoNotification('Alterado');
			}
		}

		parent::afterSave($insert, $changedAttributes); // TODO: Change the autogenerated stub
	}


	/*************************************************
	 * SENDS
	 *************************************************/
	/**
	 * @param string $moreInfo
	 * @return void
	 */
	private function sendEmailEquipamentoNotification(string $moreInfo)
	{

		$estadoEquipamento = $this->estado->estado;

		Yii::$app->mailer->compose('layouts/equipamento', ['model' => $this])
			->setFrom('equipamentos@appa8.com')
			->setTo(Yii::$app->params['equipamentoEmail'])
			->setSubject("$moreInfo Equipamento $estadoEquipamento $this->num_interno | $this->equipamento , data: " . date("d-m-Y H:i")
				. " | Utilizador: " . Yii::$app->user->identity->username)
			->setTextBody("$moreInfo Equipamento $estadoEquipamento $this->equipamento , data: " . date("d-m-Y H:i"))
			->send();
	}

