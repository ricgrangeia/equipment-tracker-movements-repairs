	/**
	 * Devolve se o equipamento está em armazem
	 * Returns if the equipment is in storage
	 * @param $equipamento_id
	 * @return boolean
	 */
	public static function isEmArmazem($equipamento_id): bool
	{

		$equipamento = EquipamentoMovimento::find()
			->Where("equipamento_id = $equipamento_id")
			->orderBy(["id" => SORT_DESC])
			->one();

		if (empty($equipamento)) return true; #Quando não existiu movimento vamos assumir que está num armazém
		if (in_array($equipamento->tipo_movimento_id, [2, 8])) return true;

		return false;
	}

	/**
	 * Devolve se o equipamento está em armazem
	 * Returns if the equipment is in storage
	 * @param $equipamento_id
	 * @return boolean
	 */
	public static function isEmReparacao($equipamento_id): bool
	{

		$equipamento = EquipamentoMovimento::find()
			->Where("equipamento_id = $equipamento_id")
			->orderBy(["id" => SORT_DESC])
			->one();

		if (empty($equipamento)) return false;

		# Tipo movimento reparação = 3
		if ($equipamento->tipo_movimento_id == 3) return true;

		return false;
	}
